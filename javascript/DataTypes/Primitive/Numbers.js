// Numbers

// JavaScript has only one type of numbers. Numbers can be written with or without decimals.
let num1 = 1;
let num2 = 22.5;
let num3 = 123e5; // 12300000
let num4 = 0xff;
let num5 = 0b11111111;
let num6 = 0o77;
let num7 = 0.55e4;

console.log(num1); // 1
console.log(num2); // 22.5
console.log(num3); // 12300000
console.log(num4); // 255
console.log(num5); // 255
console.log(num6); // 255
console.log(num7); // 255